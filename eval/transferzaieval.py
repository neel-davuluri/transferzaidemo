def build_explanation(feats, prob, precs, vccs_course, wm_code):
    parts = []
    code_short = vccs_course.split()[0] + " " + (vccs_course.split()[1] if len(vccs_course.split()) > 1 else "")
    if prob >= 0.8:
        parts.append(f"HIGH confidence ({prob:.0%}): {code_short} very likely transfers as {wm_code}.")
    elif prob >= 0.5:
        parts.append(f"MODERATE confidence ({prob:.0%}): {code_short} may transfer as {wm_code}. Verify with registrar.")
    elif prob >= 0.3:
        parts.append(f"LOW confidence ({prob:.0%}): transfer is unlikely. Do not rely on this.")
    else:
        parts.append(f"NOT EXPECTED to transfer ({prob:.0%}).")

    evidence = []
    if feats.get("dept_map_exists"):
        evidence.append(f"department mapping is established ({feats['dept_map_count']} prior transfers)")
    ce = feats.get("cross_encoder_score", 0)
    if ce > 2:
        evidence.append(f"deep semantic match is strong (score: {ce:.1f})")
    elif ce < -2:
        evidence.append(f"deep semantic match is weak (score: {ce:.1f})")
    bge_sim = feats.get("bge_cosine_sim", 0)
    if bge_sim > 0.7:
        evidence.append(f"course content is highly similar ({bge_sim:.0%} match)")
    if feats.get("same_level"):
        evidence.append("courses are at the same academic level")
    elif feats.get("level_diff", 0) >= 2:
        evidence.append(f"courses are {feats['level_diff']} levels apart (unusual)")
    if feats.get("title_seq_sim", 0) > 0.5:
        evidence.append("course titles are closely related")
    if evidence:
        parts.append("Key factors: " + "; ".join(evidence) + ".")

    approved_precs = [p for p in precs if p["decision"] == "approved"]
    if approved_precs:
        best = approved_precs[0]
        parts.append(f"Precedent: '{best['vccs_course']}' → {best['wm_code']} ({best['similarity']:.0%} similar).")
    else:
        parts.append("No strong precedent found.")
    return " ".join(parts)


def predict_transfer(vccs_course, vccs_desc, wm_code=None, top_k=5):
    vc_parsed = parse_vccs_course(vccs_course)
    vc_title = " ".join(c["title"] for c in vc_parsed) if vc_parsed else ""
    vc_desc_clean = clean_text(vccs_desc)
    vccs_text = f"{vc_title} {vc_desc_clean}"
    vccs_full = f"{vc_title} {vc_desc_clean}"

    if wm_code:
        candidates_raw = [(wm_code, 0, 0, 0)]
    else:
        vccs_emb = bge_model.encode(
            [f"Represent this sentence: {vccs_text}"], normalize_embeddings=True,
        )[0]
        candidates_raw = retrieve_top_k(vccs_text, vccs_emb, k=top_k * 3)

    # Batch cross-encoder for all candidates
    ce_pairs = []
    for cand_code, *_ in candidates_raw:
        cand_info = wm_lookup.get(cand_code, {"title": "", "description": ""})
        wm_full = f"{cand_info.get('title', '')} {clean_text(cand_info.get('description', ''))}"
        ce_pairs.append([vccs_full, wm_full])
    ce_scores_batch = cross_encoder.predict(ce_pairs) if ce_pairs else []

    results = []
    for j, (cand_code, *_) in enumerate(candidates_raw):
        cand_info = wm_lookup.get(cand_code, {"title": "", "description": ""})
        pair_row = {
            "vccs_course": vccs_course, "vccs_desc": vccs_desc,
            "wm_code": cand_code, "wm_title": cand_info.get("title", ""),
            "wm_desc": cand_info.get("description", ""),
        }
        feats = extract_features(pair_row, use_cross_encoder=False)
        feats["cross_encoder_score"] = float(ce_scores_batch[j])
        feat_vec = np.array([feats[fn] for fn in feature_names], dtype=np.float32).reshape(1, -1)
        prob = float(final_clf.predict_proba(feat_vec)[0, 1])

        precs = precedent_store.find(vccs_text, cand_code, k=3)
        explanation = build_explanation(feats, prob, precs, vccs_course, cand_code)

        results.append({
            "wm_code": cand_code, "wm_title": cand_info.get("title", ""),
            "probability": prob, "explanation": explanation,
            "precedents": precs,
            "features": {k: round(v, 4) if isinstance(v, float) else v for k, v in feats.items()},
        })

    results.sort(key=lambda x: -x["probability"])
    return results[:top_k]

