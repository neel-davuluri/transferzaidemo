RETRIEVAL_DEPTH = 50  # was 20 — this is the single biggest fix

def retrieve_top_k(vccs_text, vccs_embedding=None, k=RETRIEVAL_DEPTH, exclude_codes=None):
    """
    Retrieve top-K W&M candidates using reciprocal rank fusion.

    Changes from v1:
      - Default k=50 (was 20) to raise recall ceiling
      - Expanded RRF pool to top-200 from each retriever
      - Added title-only fallback for courses with no description
    """
    exclude_codes = exclude_codes or set()

    # BGE scores
    if vccs_embedding is None:
        vccs_embedding = bge_model.encode(
            [f"Represent this sentence: {vccs_text}"],  # fixed instruction prefix
            normalize_embeddings=True,
        )[0]
    bge_sims = WM_EMBEDDINGS @ vccs_embedding
    bge_ranked = np.argsort(bge_sims)[::-1]

    # TF-IDF scores
    tfidf_vec = tfidf.transform([vccs_text])
    tfidf_sims = cosine_similarity(tfidf_vec, wm_tfidf_matrix).flatten()
    tfidf_ranked = np.argsort(tfidf_sims)[::-1]

    # Reciprocal Rank Fusion — expanded pool
    RRF_K = 60
    rrf_scores = defaultdict(float)
    for rank, idx in enumerate(bge_ranked[:200]):  # was 100
        code = wm_codes[idx]
        if code not in exclude_codes:
            rrf_scores[code] += 1.0 / (RRF_K + rank + 1)
    for rank, idx in enumerate(tfidf_ranked[:200]):  # was 100
        code = wm_codes[idx]
        if code not in exclude_codes:
            rrf_scores[code] += 1.0 / (RRF_K + rank + 1)

    ranked = sorted(rrf_scores.items(), key=lambda x: -x[1])[:k]

    code_to_bge = {wm_codes[i]: float(bge_sims[i]) for i in range(len(wm_codes))}
    code_to_tfidf = {wm_codes[i]: float(tfidf_sims[i]) for i in range(len(wm_codes))}

    return [
        (code, rrf_score, code_to_bge.get(code, 0), code_to_tfidf.get(code, 0))
        for code, rrf_score in ranked
    ]


