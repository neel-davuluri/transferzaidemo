class PrecedentStore:
    def __init__(self, pos_df, neg_df, bge_model, tfidf, wm_lookup):
        self.bge = bge_model
        self.tfidf = tfidf
        self.wm_lookup = wm_lookup
        self.decisions = []

        for _, row in pos_df.iterrows():
            self.decisions.append({
                "vccs_course": str(row["VCCS Course"]),
                "wm_code": str(row["W&M Course Code"]).strip(),
                "vccs_text": get_vccs_text(row),
                "decision": "approved",
            })
        for _, row in neg_df.iterrows():
            self.decisions.append({
                "vccs_course": str(row["VCCS Course"]),
                "wm_code": "",
                "vccs_text": get_vccs_text(row),
                "decision": "denied",
            })

        texts = [d["vccs_text"] for d in self.decisions]
        self.embeddings = self.bge.encode(
            [f"Represent this sentence: {t}" for t in texts],
            batch_size=64, show_progress_bar=True, normalize_embeddings=True,
        )
        self.tfidf_matrix = self.tfidf.transform(texts)
        print(f"Precedent store: {len(self.decisions)} decisions")

    def find(self, vccs_text, wm_code=None, k=5):
        q_emb = self.bge.encode(
            [f"Represent this sentence: {vccs_text}"], normalize_embeddings=True,
        )[0]
        bge_sims = self.embeddings @ q_emb
        q_tfidf = self.tfidf.transform([vccs_text])
        tfidf_sims = cosine_similarity(q_tfidf, self.tfidf_matrix).flatten()
        combined = 0.7 * bge_sims + 0.3 * tfidf_sims
        ranked = np.argsort(combined)[::-1]
        results = []
        for idx in ranked:
            if len(results) >= k:
                break
            sim = float(combined[idx])
            if sim < 0.1:
                break
            d = self.decisions[idx]
            result = {"vccs_course": d["vccs_course"], "wm_code": d["wm_code"],
                      "decision": d["decision"], "similarity": sim}
            if wm_code:
                result["same_target"] = d["wm_code"] == wm_code
            results.append(result)
        return results

print("\nBuilding precedent store...")
